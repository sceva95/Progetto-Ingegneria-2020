    private void aggiungiLibroButtonActionPerformed(java.awt.event.ActionEvent evt) {
        String titolo ="";
        String autore ="";
        String casa = "";
        int anno = 0;
        String genere ="";
        double prezzo = 0;
        String isbn ="";
        String descrizione ="";
        int punti = 0;
        try{
            titolo = titoloText.getText();
            autore = autoreText.getText();
            casa = casaEditriceText.getText();
            anno = Integer.parseInt(annoText.getText());
            genere = genereBox.getSelectedItem().toString();
            prezzo= Double.parseDouble(prezzoText.getText());
            isbn = isbnText.getText();
            descrizione = descrizioneText.getText();
            punti = Integer.parseInt(puntiText.getText());
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Campi inseriti non corretti");
            return;
        }
        Libro nuovo = new Libro();
        nuovo.setTitolo(titolo);
        nuovo.setAutore(autore);
        nuovo.setCasaEditrice(casa);
        nuovo.setAnno(anno);
        nuovo.setGenere(genere);
        nuovo.setPrezzo(prezzo);
        nuovo.setIsbn(isbn);
        nuovo.setDescrizione(descrizione);
        nuovo.setPunti(punti);
        nuovo.setTempo(0);
        nuovo.setVendite(0);
        try{
            main.getController().getLibroController().aggiungi(nuovo);
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Impossibile aggiungere il libro");
            return;
        }
        JOptionPane.showMessageDialog(null, "Libro aggiunto!");
        main.setLibri(main.getController().getLibroController().findAll());
        main.updateTable();


    }

    private void updateTableOrdini(){
        ordini = main.getController().getOrdineController().findAll();
        int count = 0;
        DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
        int rowCount = dm.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
        }
        for(Ordine tmp: ordini){
            dm.addRow(new Object[]{tmp.getIdentificativo()});
            jTable1.setValueAt(tmp.getIdentificativo(), count,0);
            jTable1.setValueAt(tmp.getEmail(), count,1);
            jTable1.setValueAt(tmp.getStato(), count,2);

            count++;
        }
    }

    private void updateTableUtenti(){
        utenti = main.getController().getUtenteController().findUtentiRegistrati("REGISTRATO");
        int count = 0;
        DefaultTableModel dm = (DefaultTableModel) jTable2.getModel();
        int rowCount = dm.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
        }
        for(Utente tmp: utenti){
            dm.addRow(new Object[]{tmp.getEmail()});
            jTable2.setValueAt(tmp.getEmail(), count,0);
            jTable2.setValueAt(tmp.getLibroCardPunti(), count,1);
            count++;
        }
    }


