    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        //modifica l'ordine selezionato e aggiorna la tabella ordini
        DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
        int id = (int) dm.getValueAt(jTable1.getSelectedRow(), 0);
        Ordine modificato = new Ordine();
        try{
            modificato = main.getController().getOrdineController().findById(id);
            modificato.setStato(statoBoc.getSelectedItem().toString());
            main.getController().getOrdineController().save(modificato);
            JOptionPane.showMessageDialog(null, "Ordine modificato!");
            updateTableOrdini();
        }catch (Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Qualcosa è andato storto!");
        }


    }

