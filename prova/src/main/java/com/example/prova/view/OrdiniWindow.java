    private void updateTableOrdini(){
        int count = 0;
        DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
        int rowCount = dm.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
        }
        for(Ordine tmp: ordini){
            dm.addRow(new Object[]{tmp.getIdentificativo()});
            jTable1.setValueAt(tmp.getIdentificativo(), count,0);
            jTable1.setValueAt(tmp.getData(), count,1);
            jTable1.setValueAt(tmp.getCosto(), count,2);
            jTable1.setValueAt(tmp.getPagamento(), count,3);
            jTable1.setValueAt(tmp.getPunti(), count,4);
            jTable1.setValueAt(tmp.getStato(), count,5);
            count++;
        }
    }

    private void ispezionaButtonActionPerformed(ActionEvent ae){

        int row = jTable1.getSelectedRow();
        int id = (Integer)jTable1.getValueAt(row, 0);
        DefaultTableModel tableModel = (DefaultTableModel)jTable2.getModel();
        int rowCount = tableModel.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            tableModel.removeRow(i);
        }
        Object[] result = new Object[6];
        libri = main.getController().getOrdineLibroController().findByIdOrdine(id);
        Libro libro = new Libro();
        for(OrdineLibro tmp: libri){
            libro = main.getController().getLibroController().findById(tmp.getId().getIsbn());
            result[0] = libro.getTitolo();
            result[1] =libro.getAutore();
            result[2] = tmp.getQuantita();
            result[3] = libro.getPrezzo();
            result[4] = libro.getPunti();
            tableModel.addRow(result);
        }
    }
